function extractInner(svgContent) {
  // Strip the outer <svg ...> wrapper and return the inner markup
  return svgContent
    .replace(/<svg[^>]*>/, '')
    .replace(/<\/svg>\s*$/, '')
    .trim();
}

const symbols = [];

for (const [weight, names] of Object.entries(ICONS)) {
  for (const name of names) {
    const filePath = resolve(assetsDir, weight, `${name}-${weight}.svg`);
    let raw;
    try {
      raw = readFileSync(filePath, 'utf8');
    } catch {
      console.warn(`Warning: missing ${filePath} — skipping symbol ph-${weight}-${name}`);
      continue;
    }
    const inner = extractInner(raw);
    symbols.push(
      `  <symbol id="ph-${weight}-${name}" viewBox="0 0 256 256">${inner}</symbol>`
    );
  }
}

const sprite = [
  '{{- /* Auto-generated by scripts/build-icon-sprite.mjs — do not edit by hand */ -}}',
  '<svg xmlns="http://www.w3.org/2000/svg" width="0" height="0" style="position:absolute;overflow:hidden" aria-hidden="true" focusable="false">',
  ...symbols,
  '</svg>',
].join('\n');

writeFileSync(outFile, sprite, 'utf8');
console.log(`Wrote ${symbols.length} symbols → ${outFile}`);
